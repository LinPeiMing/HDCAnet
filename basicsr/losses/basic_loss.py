@LOSS_REGISTRY.register()
class HuberLoss(nn.Module):
    def __init__(self, loss_weight=1.0, delta=0.01, reduction='mean'):
        super(HuberLoss, self).__init__()
        self.loss_weight = loss_weight
        self.reduction = reduction
        self.delta = delta
    def forward(self, pred, target, **kwargs):
        abs_error = torch.abs(pred - target)
        quadratic = torch.clamp(abs_error, max=self.delta)
        linear = (abs_error - quadratic)
        losses = 0.5 * torch.pow(quadratic, 2) + self.delta * linear
        return self.loss_weight * torch.mean(losses)


@LOSS_REGISTRY.register()
class BTLoss(nn.Module):
    def __init__(self, lambda_param=1e-3):
        super(BTLoss, self).__init__()
        self.lambda_param = lambda_param

    def forward(self, z_a: torch.Tensor, z_b: torch.Tensor):
        # normalize repr. along the batch dimension
        z_a_norm = (z_a - z_a.mean(0)) / z_a.std(0)  # NxD
        z_b_norm = (z_b - z_b.mean(0)) / z_b.std(0)  # NxD
        N = z_a.size(0)
        D = z_a.size(1)
        # cross-correlation matrix
        c = torch.mm(z_a_norm.t(), z_b_norm) / N  # DxD
        # # loss
        c_diff = (c - torch.eye(D).to('cuda')).pow(2)  # DxD
        on_diag = torch.diagonal(c_diff).sum()
        off_diag = off_diagonal(c_diff).sum() * self.lambda_param
        loss = on_diag + off_diag
        return loss


@LOSS_REGISTRY.register()
class ComposeLoss(nn.Module):
    def __init__(self, loss_weight=0.05, reduction='mean'):
        super(ComposeLoss, self).__init__()
        self.loss1 = L1Loss()
        # self.loss1 = MSELoss(reduction=reduction)
        self.loss2 = FFTCharLoss()
        self.loss_weight = loss_weight

    def forward(self,pred, target):
        loss1 = self.loss1(pred, target)
        loss2 = self.loss2(pred, target)

        return loss1+self.loss_weight*loss2


@LOSS_REGISTRY.register()
class FFTCharLoss(nn.Module):
    def __init__(self, loss_weight=1.0, eps=1e-12, reduction='mean'):
        super(FFTCharLoss, self).__init__()
        if reduction not in ['none', 'mean', 'sum']:
            raise ValueError(f'Unsupported reduction mode: {reduction}. '
                             f'Supported ones are: {_reduction_modes}')
        self.loss_weight = loss_weight
        self.reduction = reduction
        self.eps = eps

    def forward(self, pred, target, weight=None, **kwargs):
        pred_fft = torch.fft.rfft2(pred, norm='backward')
        target_fft = torch.fft.rfft2(target, norm='backward')
        diff_real = torch.sqrt((pred_fft.real - target_fft.real)**2 + self.eps)
        diff_imag = torch.sqrt((pred_fft.imag - target_fft.imag)**2 + self.eps)
        freq_distance = diff_real + diff_imag
        return self.loss_weight*torch.mean(freq_distance)
